import express from "express";
import {
  addPostReport,
  deleteReportPost,
  getAllReportPost,
  updateSavePost,
} from "../controllers/reportPost.controller";

const reportPostRoutes = express.Router();

reportPostRoutes.post( "/", addPostReport);

reportPostRoutes.get("/", getAllReportPost);
reportPostRoutes.put("/:id", updateSavePost);
reportPostRoutes.delete("/:id", deleteReportPost);

export default reportPostRoutes;
